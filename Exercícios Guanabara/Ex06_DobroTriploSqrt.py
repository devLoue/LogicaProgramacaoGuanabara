# Dobro triplo e raiz quadrada

#Importando squareroot (raiz quadrada) da biblioteca math
from math import sqrt
#Entrada de dados
n1 = int(input('Digite um valor:'))
#Saída de dados e operações matemáticas
print(f'o dobro de {n1} é {n1*2}')
print(f'o triplo de {n1} é {n1*3}')
print(f'a raiz quadrada de {n1} é {sqrt(n1)}')