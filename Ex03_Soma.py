#Exercício 3 - Soma de variavéis 

n1 = int(input('Digite um valor:')) # int = inteiro, estamos declarando ao python nesse input que o dado digitado pelo usuário vai ser um número do tipo inteiro na variável "n1"
n2 = int(input('Digite outro valor: ')) # nessa linha criamos uma segunda variável denominada "n2"
print(f'{n1} + {n2} = {n1+n2}') # Soma das duas variáveis sendo exibidas através do comando print