#Entrada de dados
nome = input('Qual é o seu nome? \n') #A função input pede para que o usuário retorne uma string, neste caso, pedimos o seu nome e o guardamos na variável chamada "nome"
print (f'Olá, {nome}') # a função print exibe a mensagem Olá, nome (variável em que guardamos o nome que o usuário digitar no input)

