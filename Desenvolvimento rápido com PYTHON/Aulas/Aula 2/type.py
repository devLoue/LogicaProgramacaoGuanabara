## Soma simples de dois números inteiros
soma = 2 + 2
print(soma)
print(type(soma)) 
## type mostra o tipo da variável soma, que neste caso, é um inteiro. 

##Concatenando
soma = "2" + "2"
print(soma) ##Neste caso, a soma também é "22", porém, foi feita uma concatenação de string. A  variável soma recebe a string "2"+ a string "2", mostrando como 22. 
print(type(soma)) ## Como foi dito na linha de cima, soma agora é uma string, e a função type nos mostra isso. 